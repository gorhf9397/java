package java0516;

public class Op1 {

	public static void main(String[] args) {
		// 산술연산 => + - * / %(나머지 구하는 수식)
		System.out.println(10+10);
		System.out.println(10-10);
		System.out.println(10*10);
		System.out.println(10/10);
		System.out.println(10%10);
		
		//변수에 값을 산술연산
		int aa=55;
		int bb=43;
		System.out.println(aa+bb);
		System.out.println(aa-bb);
		System.out.println(aa*bb);
		System.out.println(aa/bb);
		System.out.println(aa%bb);

	}

}
