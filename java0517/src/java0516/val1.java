package java0516;

public class val1 {

	public static void main(String[] args) {
		// 변수의 종류
		// 숫자형 => 정수 : byte, short, int, long
		// 숫자형 => 실수 : double, float
		// 문자형 => 문자1자 : char, 문자열 : String
		// 논리형 => 참 or 거짓 : boolean
		
		// 변수를 메모리에 만들기 => 변수 선언 => 자료형 변수명;
		// 변수 선언
		byte b1;  // 1byte 공간에 저장할 값 // ;(세미클론)은 한 문장이 끝날때 표시
		short s1; // 2byte 공간에 저장할 값
		int aa;   // 4byte 공간에 저장할 값
		long bb;  // 8byte 공간에 저장할 값
		
		float f1; // 4byte
		double d1; // 8byte
		
		char c1; // 문자 1자 저장
		String s2; // 문자를 1개 이상
		
		boolean bb2; // true, false 를 저장
		
	}

}
