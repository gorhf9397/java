
public class comp1 {

	public static void main(String[] args) {
		// 비교연산자 => 2개의 요소를 비교하는 연산자 => 결과는 true, false로 가져온다.
		
		System.out.println(10 > 20);
		System.out.println(10 < 20);
		System.out.println(10 == 20);
		
		System.out.println(10 >= 20);// 크거나같다
		System.out.println(10 <= 20);// 작거나같다
		
		System.out.println(10 != 20);// 같은게 아니다
		
		

	}

}
