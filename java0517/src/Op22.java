
public class Op2 {

	public static void main(String[] args) {
		// 증감 연산자 => ++, --
		
		int x=10;
		System.out.println(x);
		
		x=x+1; 
		//x변수에다 1을 더하기
		//더한값을 x변수의 저장
		System.out.println(x);
		
		int y=10;
		System.out.println(y);
		
		y=y-10;
		System.out.println(y);

	}

}